export function getNewsBySlug(slug: string): NewsItem | undefined {
  return NEWS_ITEMS.find((n) => n.slug === slug)
}

export function getNewsByCategory(category?: string): NewsItem[] {
  if (!category || category === 'all') return NEWS_ITEMS
  return NEWS_ITEMS.filter((n) => n.category === category)
}

export function getFeaturedNews(): NewsItem | undefined {
  return NEWS_ITEMS.find((n) => n.featured)
}
